# NoneType variable type:
none = None
print(none)
print(type(none))

# str variable type:
string = "String value"
print(string)
print(type(string))

# int variable type:
int = 23
print(int)
print(type(int))

# float variable type:
float = -23.56
print(float)
print(type(float))

# bool variable type:
boolean = True
print(boolean)
print(type(boolean))

# list variable type:
array = [10,20.3,"Hi", False, True]
print(array)
print(type(array))

# tuple variable type: any of its values does not change
tuple = (1,2,3,4,5)
print(tuple)
print(type(tuple))

# Dictionary (dict): like json objects
dictionary = {
    "element_A": "value_A",
    "element_B": 23,
    "element_C": True
}
print(dictionary["element_A"])
print(type(dictionary))

# range:
range = range(9)
print(range)
print(type(range))

# bytes:
byte = b"Byte message"
print(byte)
print(type(byte))